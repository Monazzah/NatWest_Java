package com.natwest.calculatortask;

public class Calculator {
	public double addTwo(double number1, double number2) {
		return number1 + number2;
	}

	public double subTwo(double number1, double number2) {
		return number1 - number2;
	}

	public double mulTwo(double number1, double number2) {
		return number1 * number2;
	}

	public double divTwo(double number1, double number2) {
		return number1 / number2;
	}

}
